public class variables {
    public static void main(String [] args){

        // DEFINE OR INITIALIZE

        int age;
        age=15;
        age=19;

       // CASE SENSITIVE 

        int weight=75;
        int WEIGHT= 67;
        System.out.println(age);
        System.out.println("Weight"+weight);
        System.out.println("WEIGHT"+WEIGHT);

        // STARTING LETTER

        int marks=90;
        int _marks=88;
        int Marks=80;
        int $marks=85;
        System.out.println(marks);
        System.out.println(_marks);
        System.out.println(Marks);
        System.out.println($marks);

        // SUBSEQUENT LETTERS\

        int marks5=110;
        int marks_=55;
        int marks_a=100;
        int marks$a=112;
        System.out.println(marks5);
        System.out.println(marks_);
        System.out.println(marks_a);
        System.out.println(marks$a);

        // CAMELCASE


        int myNameIsArnavKushwaha=5500000;
        System.out.println(myNameIsArnavKushwaha);
    }
    
}
